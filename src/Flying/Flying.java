package Flying;

public abstract class Flying
{
    private int xwidth;
    private int ylength;

    public void setWidth(int xwidth)
    {
        this.xwidth = xwidth;
    }

    public void setLength(int ylength)
    {
        this.ylength = ylength;
    }

    public int getXwidth()
    {
        return xwidth;
    }
    public int getYLength()
    {
        return ylength;
    }
}
